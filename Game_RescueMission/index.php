                <h3 class="section-subheading text-muted">Rescue Mission is a top-down view 2D game. In this game,
                    the player uses a gun to kill the terrorist gang and save the hostage.</h3>
            </div>
        </div>
        <div class="row text-center">
            <div class="col-md-4">

                <h4 class="service-heading">Game Story</h4>
                <p class="text-muted" align="justify">During a terrorist attack, a building was completely hijacked by those international criminals.
                    Two top skilled FBI special agents James and Lucy were sent for the rescue mission.
                    They entered the building from different entrances. Unfortunately, Lucy was captured by the terrorist gang
                    and was kept in a room in the second floor. Later, Lucy discovered that a timer bomb was installed in her room
                    and it starts counting when the room door is opened without a key. The objective of James is to kill all the terrorists using his gun,
                    find the key to deactivate the bomb and save Lucy. </p>
            </div>
            <div class="col-md-4">

                <h4 class="service-heading">Game Challenges</h4>
                <p class="text-muted" align="justify">The main challenge is the player must protect himself from being killed by the terrorist gang.
                    The terrorists starts shooting at the player when the player comes near a terrorist.
                    The player must kill all the terrorists and collect all the coins to enter the next level.
                    In the second level, the player can't open the door of the room where the hostage is kept until the player
                    has the key to open. If opened without a key, the bomb attached with the door gets activated.
                    The player must find the key to deactivate the bomb before the timer goes out.</p>
            </div>
            <div class="col-md-4">

                <h4 class="service-heading">How to Play</h4>
                <p class="text-muted" align="justify">The player can walk in all directions using Arrow Keys. The player has a gun and can shoot
                    by pressing the Space Bar. The score points increases when the player kills a terrorist, collects a coin
                    and collects the key. The player has a health bar over the head to show the remaining health.
                    There is no option for the player to jump. The player  needs to find a key to open a door in second level
                    and diffuse a bomb.</p>
            </div>
        </div>
    </div>
